package chapter2;

/*
 ** Преобразование строк к значению: A12
 */

public class Exercise_23_1 {
    public static void main(String[] args) {
        System.out.println('A' + '1' + "2");
        System.out.println("A" + 12);
        System.out.println("A" + ('\t' + '\u0003'));
        System.out.println('A' + "12");
    }
}
